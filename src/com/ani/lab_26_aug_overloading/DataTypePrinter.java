package com.ani.lab_26_aug_overloading;

public class DataTypePrinter 
{
	public void printData(byte type)
	{
		System.out.println("byte :"+type);
	}
	
	public void printData(short type)
	{
		System.out.println("short :"+type);
	}
	
	public void printData(int type)
	{
		System.out.println("int :"+type);
	}
	
	public void printData(long type)
	{
		System.out.println("long :"+type);
	}
	
	
	public void printData(float type)
	{
		System.out.println("float :"+type);
	}
	
	public void printData(double type)
	{
		System.out.println("double :"+type);
	}
	
	public void printData(char type)
	{
		System.out.println("char :"+type);
	}
	
	public void printData(boolean type)
	{
		System.out.println("boolean :"+type);
	}
	
	public void printData(String type)
	{
		System.out.println("String :"+type);
	}
	
}
